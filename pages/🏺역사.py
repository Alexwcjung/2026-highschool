def show_history(data):
    st.markdown(
        f"""
        <div class="intro-box">
            <h2>{data['emoji']} {data['title']}</h2>
            <p>{data['intro']}</p>
        </div>
        """,
        unsafe_allow_html=True
    )

    st.markdown("<div class='section-title'>👤 건국 인물 / 나라 형성의 핵심 인물</div>", unsafe_allow_html=True)
    for founder in data["founders"]:
        st.markdown(
            f"""
            <div class="simple-item founder-item">
                <h3>{founder['name']}</h3>
                <div class="meta">{founder['role']}</div>
                <p>{founder['detail']}</p>
            </div>
            """,
            unsafe_allow_html=True
        )

    st.markdown("<div class='section-title'>🕰️ 시대 흐름</div>", unsafe_allow_html=True)
    for period in data["periods"]:
        tags = "".join([f"<span class='tag'>{kw}</span>" for kw in period["keywords"]])
        st.markdown(
            f"""
            <div class="simple-item period-item">
                <h3>{period['name']}</h3>
                <div class="meta">{period['time']}</div>
                <p>{period['summary']}</p>
                <div>{tags}</div>
            </div>
            """,
            unsafe_allow_html=True
        )

    st.markdown("<div class='section-title'>📌 주요 사건</div>", unsafe_allow_html=True)
    for event in data["events"]:
        st.markdown(
            f"""
            <div class="simple-item event-item">
                <h3>{event['name']}</h3>
                <div class="meta">{event['date']}</div>
                <p>{event['detail']}</p>
            </div>
            """,
            unsafe_allow_html=True
        )

    st.markdown(
        f"""
        <div class="summary-box">
            <b>핵심 정리</b><br>
            {data['summary']}
        </div>
        """,
        unsafe_allow_html=True
    )

