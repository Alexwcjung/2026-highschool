def show_history_tab(data):
    st.markdown(
        f"""
        <div class="intro-box">
            <h2>{data['title']}</h2>
            <p><b>{data['subtitle']}</b></p>
            <p>{data['intro']}</p>
        </div>
        """,
        unsafe_allow_html=True
    )

    flow_text = " → ".join(data["flow"])
    st.markdown(
        f"""
        <div class="flow-box">
            🕰️ <b>역사 흐름:</b><br>{flow_text}
        </div>
        """,
        unsafe_allow_html=True
    )

    rows = []
    for item in data["periods"]:
        rows.append({
            "시대": item["name"],
            "시기": item["period"],
            "핵심 키워드": ", ".join(item["keywords"]),
            "쉬운 설명": item["easy"]
        })

    st.markdown("### 📋 한눈에 정리")
    st.dataframe(rows, use_container_width=True, hide_index=True)

    st.markdown("### 🧩 시대별 핵심 카드")
    cols = st.columns(2)
    for i, item in enumerate(data["periods"]):
        tags = "".join([f"<span class='tag'>{kw}</span>" for kw in item["keywords"]])
        with cols[i % 2]:
            st.markdown(
                f"""
                <div class="period-card">
                    <h3>{item['name']}</h3>
                    <p><b>시기:</b> {item['period']}</p>
                    <p><b>핵심 설명:</b><br>{item['summary']}</p>
                    <p><b>쉽게 말하면:</b><br>{item['easy']}</p>
                    <div>{tags}</div>
                </div>
                """,
                unsafe_allow_html=True
            )

    st.markdown(
        f"""
        <div class="key-box">
            ✅ 핵심 정리: {data['key_sentence']}
        </div>
        """,
        unsafe_allow_html=True
    )

